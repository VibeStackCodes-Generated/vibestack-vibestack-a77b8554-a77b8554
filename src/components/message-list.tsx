interface Message { id: string; content: string; user_id: string; created_at: string; }

export function MessageList({ messages }: { messages: Message[] }) {
  if (messages.length === 0) {
    return <div className="flex items-center justify-center h-full text-[hsl(var(--muted-foreground))]">No messages yet.</div>;
  }
  return (
    <div className="space-y-4">
      {messages.map((msg) => (
        <div key={msg.id} className="flex flex-col">
          <div className="bg-[hsl(var(--muted))] rounded-[var(--radius)] p-3 max-w-[80%]">
            <p className="text-sm">{msg.content}</p>
          </div>
          <span className="text-xs text-[hsl(var(--muted-foreground))] mt-1">{new Date(msg.created_at).toLocaleTimeString()}</span>
        </div>
      ))}
    </div>
  );
}
